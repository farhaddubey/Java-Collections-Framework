import java.util.ArrayList;

public class ArrayList2{
    public static void main(String[] args){
        ArrayList<Double> list1=new ArrayList<>();
        // Primitive Data Types |   Wrapper Classes
        // int-------------------->Integer
        // char------------------->Character
        // long------------------->Long
        // string------------------>String
        // float-------------------> Float
        // double------------------>Double
        // boolean----------------->Boolean

        list1.add(1.5);
        list1.add(2.5);
        list1.add(3.5);
        list1.add(4.5);
        System.out.println(list1);
    }
}